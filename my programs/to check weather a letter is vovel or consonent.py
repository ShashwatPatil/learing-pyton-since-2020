# Python Program to check character is Vowel or Consonant
ch = input("enter your letter : ")

if ch == 'a' or ch == 'e' or ch == 'i' or ch == 'o' or ch == 'u' or ch == 'A'\
        or ch == 'E' or ch == 'I' or ch == 'O' or ch == 'U':
    print("it is a VOWEL")
else:
    print("it is a CONSONANT")

