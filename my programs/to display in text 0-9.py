# To display in text 0-9

a = int(input("enter the number :"))

if a == 0:
    print("ZERO")
elif a == 1:
    print("ONE")
elif a == 2:
    print("TWO")
elif a == 3:
    print("THREE")
elif a == 4:
    print("FOUR")
elif a == 5:
    print("FIVE")
elif a == 6:
    print("SIX")
elif a == 7:
    print("SEVEN")
elif a == 8:
    print("EIGHT")
elif a == 9:
    print("NINE")
else:
    print("out of range ,the range is 0 to 9")
